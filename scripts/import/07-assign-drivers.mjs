function normPlate(p) {
  return p.replace(/\s+/g, " ").trim().toUpperCase();
}
function normName(n) {
  let s = n.replace(/\s+/g, " ").trim().toUpperCase();
  s = s.replace(/\bFREMORE\b/, "FREDMORE").replace(/\bELLAIH\b/, "ELLIAH");
  return s;
}

// tally[plate][driver] = count
const tally = {};
let rows = 0;
for (const line of RAW.split(/\r?\n/)) {
  if (!line.trim()) continue;
  const parts = line.split("\t");
  if (parts.length < 2) continue;
  const plate = normPlate(parts[0]);
  const driver = normName(parts[1]);
  if (!plate || !driver) continue;
  rows++;
  (tally[plate] ||= {});
  tally[plate][driver] = (tally[plate][driver] || 0) + 1;
}

async function main() {
  const c = new pg.Client({ connectionString: process.env.DATABASE_URL, ssl: { rejectUnauthorized: false } });
  await c.connect();
  const dRes = await c.query(
    "select dr.id, p.full_name from app.drivers dr join app.profiles p on p.id=dr.profile_id where dr.is_active",
  );
  const vRes = await c.query("select id, plate_number from app.vehicles");
  await c.end().catch(() => {});

  const driversByUpper = new Map(dRes.rows.map((r) => [r.full_name.toUpperCase(), r.id]));
  const driverFirstToken = new Map(); // FIRSTTOKEN -> [{name,id}]
  for (const r of dRes.rows) {
    const t = r.full_name.toUpperCase().split(" ")[0];
    (driverFirstToken.get(t) || driverFirstToken.set(t, []).get(t)).push({ name: r.full_name, id: r.id });
  }
  const vehicleByPlate = new Map(vRes.rows.map((r) => [normPlate(r.plate_number), r.id]));

  function matchDriver(name) {
    if (driversByUpper.has(name)) return { id: driversByUpper.get(name), db: name };
    if (!name.includes(" ")) {
      const cand = driverFirstToken.get(name);
      if (cand && cand.length === 1) return { id: cand[0].id, db: cand[0].name.toUpperCase() };
    }
    return null;
  }

  const plan = [];
  const unmatchedPlates = [];
  const unmatchedDrivers = new Set();

  for (const plate of Object.keys(tally).sort()) {
    const drivers = Object.entries(tally[plate]).sort((a, b) => b[1] - a[1]);
    const [topName, topCount] = drivers[0];
    const vId = vehicleByPlate.get(plate);
    if (!vId) {
      unmatchedPlates.push(`${plate} (top: ${topName} ×${topCount})`);
      continue;
    }
    const dm = matchDriver(topName);
    if (!dm) {
      unmatchedDrivers.add(topName);
      plan.push({ plate, vId, topName, topCount, driverId: null, dbName: null, breakdown: drivers });
      continue;
    }
    plan.push({ plate, vId, topName, topCount, driverId: dm.id, dbName: dm.db, breakdown: drivers });
  }

  console.log(`Parsed ${rows} usage rows across ${Object.keys(tally).length} plates.\n`);
  console.log("PROPOSED DEFAULT (most-driven) ASSIGNMENTS:");
  for (const p of plan) {
    const bd = p.breakdown.map(([n, ct]) => `${n}×${ct}`).join(", ");
    console.log(`  ${p.plate} -> ${p.dbName ?? "‼ " + p.topName + " (NOT IN DB)"}   [${bd}]`);
  }
  if (unmatchedPlates.length) console.log("\nPlates not in DB (skipped):\n  " + unmatchedPlates.join("\n  "));
  if (unmatchedDrivers.size) console.log("\nDrivers not in DB (skipped):\n  " + [...unmatchedDrivers].join("\n  "));

  if (!APPLY) {
    console.log("\n(report only — re-run with --apply to write to the database)");
    return;
  }

  // ---- APPLY ----
  const c2 = new pg.Client({ connectionString: process.env.DATABASE_URL, ssl: { rejectUnauthorized: false } });
  await c2.connect();
  let reassigned = 0, kept = 0, ended = 0;
  for (const p of plan) {
    if (!p.driverId) continue;
    const cur = await c2.query(
      "select id, driver_id from app.vehicle_assignments where vehicle_id=$1 and ended_at is null",
      [p.vId],
    );
    const already = cur.rows.find((r) => r.driver_id === p.driverId);
    if (already) {
      kept++;
      continue;
    }
    await c2.query("begin");
    try {
      for (const r of cur.rows) {
        await c2.query("update app.vehicle_assignments set ended_at=now() where id=$1", [r.id]);
        ended++;
      }
      await c2.query(
        "insert into app.vehicle_assignments (vehicle_id, driver_id, started_at, notes) values ($1,$2,now(),$3)",
        [p.vId, p.driverId, "Default from Apr–Jun 2026 usage (most-driven)"],
      );
      await c2.query("commit");
      reassigned++;
    } catch (e) {
      await c2.query("rollback");
      console.error(`  ✗ ${p.plate} -> ${p.dbName}: ${e.message}`);
    }
  }
  await c2.end();
  console.log(`\nAPPLIED: ${reassigned} reassigned, ${kept} already correct, ${ended} prior assignments ended.`);
}

main().catch((e) => {
  console.error("ERR", e.message);
  process.exit(1);
});
